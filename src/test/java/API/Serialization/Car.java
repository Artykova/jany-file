package API.Serialization;

public class Car {
    private String model;
    private int age;
    private String status;
    private int id;
    private String photoUrl;

    public int getAge() {
        return age;
    }

    public String getModel() {
        return model;
    }

    public int getId() {
        return id;
    }

    public String getPhotoUrl() {
        return photoUrl;
    }

    public String getStatus() {
        return status;
    }
}